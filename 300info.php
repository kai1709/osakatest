      <td>This level is appropriate for those who:
        <br> ・Have successfully completed the beginner's course (for example, those who have completed texts such as &quot;Minna
        no Nihongo II&quot;, &quot;Genki II&quot;, or &quot;Situational Functional Japanese II&quot;).
        <br> ・Can hold simple, everyday conversation and read and write simple sentences about familiar things.
        <br> ・Can comprehend Japanese when it is spoken slowly and clearly, but have trouble understanding Japanese at a normal
        rate of speed.
        <br> ・Can read about 200-300 kanji.
        <br>
        <br> ・ 初級日本語コース（例：「みんなの日本語Ⅱ」「げんきⅡ」「Situational Functional Japanese Ⅱ」などの教科書）を終わったレベルです。
        <br> ・ 簡単な日常会話や、身近な話題についての短い文章の読み書きができます。
        <br> ・ ゆっくりわかりやすく話してもらうと聞きとれますが、普通の速さの話を聞くのは難しいです。
        <br> ・ 200～300字程度の漢字を読むことができます。</td>
